    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String nombre = txtNombrePropietario.getText().trim();
        String documento = txtNumeroDocumento.getText().trim();

        if (nombre.isEmpty() || documento.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa el nombre y numero de documento");
            return;
        }

        Propietario propi = daoPro.buscarPropietario(documento, nombre);
        if (propi != null) {
            // Llenar campos
            cbTipoDocumento.setSelectedItem(propi.getTipoDocumento());
            txtTelefono.setText(propi.getTelefono());
            txtDireccion.setText(propi.getDireccion());
            txtEmail.setText(propi.getEmail());
            JOptionPane.showMessageDialog(this, "Propietario encontrado");
        } else {
            JOptionPane.showMessageDialog(this, "No se encontro el propietario");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            // Crear el DTO con los datos del formulario
            PropietarioDTO dto = new PropietarioDTO();
            dto.setNombreP(txtNombrePropietario.getText().trim());
            dto.setTipoDocumento((String) cbTipoDocumento.getSelectedItem()); // CORREGIDO
            dto.setNumeroDocumento(txtNumeroDocumento.getText().trim());
            dto.setTelefono(txtTelefono.getText().trim());
            dto.setDireccion(txtDireccion.getText().trim());
            dto.setEmail(txtEmail.getText().trim());

            controladorPropi.guardarPropietario(dto); // Validación ocurre aquí

            JOptionPane.showMessageDialog(this, "Propietario guardado exitosamente.");
            JOptionPane.showMessageDialog(this,
                    "-----------Propietario registrado--------------\n"
                    + "Nombre: " + dto.getNombreP() + "\n"
                    + "Tipo de Documento: " + dto.getTipoDocumento() + "\n"
                    + "Numero de Documento: " + dto.getNumeroDocumento() + "\n"
                    + "Telefono: " + dto.getTelefono() + "\n"
                    + "Direccion: " + dto.getDireccion() + "\n"
                    + "Email: " + dto.getEmail()
            );
            limpiarCampos();
            llenarTabla(); // Si deseas actualizar tabla
        } catch (DatoInvalidoException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error inesperado: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        String nombreP = txtNombrePropietario.getText().trim();
        String tipoDocumento = cbTipoDocumento.getSelectedItem().toString();
        String documento = txtNumeroDocumento.getText().trim();
        String telefono = txtTelefono.getText().trim();
        String direccion = txtDireccion.getText().trim();
        String email = txtEmail.getText().trim();

        if (nombreP.isEmpty() || documento.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar nombre y numero de documento para editar");
            return;
        }

        Propietario actualizado = new Propietario(nombreP, tipoDocumento, documento, telefono, direccion, email);

        if (daoPro.editarPropietario(actualizado)) {
            JOptionPane.showMessageDialog(this, "Propietario editado");
            llenarTabla();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo editar", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String documento = txtNumeroDocumento.getText().trim();
        try {
            controladorPropi.eliminarPropietario(documento);
            JOptionPane.showMessageDialog(this, "Propietario eliminado correctamente");
            limpiarCampos();
        } catch (DatoInvalidoException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

//-----------creo metodo de limpiar campos----------------------
    private void limpiarCampos() {
        txtNombrePropietario.setText("");
        cbTipoDocumento.setSelectedIndex(0);
        txtNumeroDocumento.setText("");
        txtTelefono.setText("");
        txtDireccion.setText("");
        txtEmail.setText("");

    }
//------------creo metodo de llenar tabla------------------------

    private void llenarTabla() {

        DefaultTableModel modelo = (DefaultTableModel) tblPropietarios.getModel();
        modelo.setRowCount(0); // Limpiar la tabla

        for (Propietario p : daoPro.listar()) {
            modelo.addRow(new Object[]{
                p.getNombreP(),
                p.getTipoDocumento(),
                p.getNumeroDocumento(),
                p.getTelefono(),
                p.getDireccion(),
                p.getEmail()
            });
        }
    }

